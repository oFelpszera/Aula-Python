"""
TIPO DE DADO "bool" (Boolean)
Representa um "questionamento", onde somente duas respostas são possíveis
"True" -> Sim ou "False" -> Não
Existem vários "operadores" para "questionar" se uma informção é "True" ou "False"
Um deles é o "==" que é um operador lógico que diz se um valor é igual ao outro.
"""
print('Felps' == 'Felps') # Sim -> "True" -> Verdadeiro
print('Felps' == 'Felipe') # Não -> "False" -> Falso
print('Felps' == 'Felps', type(10 == 10)) # Vai me dizer que é uma classe "bool"
print('Felps' == 'Felipe', type(False)) # Vai me dizer que é uma classe "bool"