# OPERADORES ARITMETICOS SEM PRECISAR REPETIR A VARIAVEL
# += ; -= ; /= ; //= / *= ; **= ; %=

variavel = 10

variavel += 10
# SOMA o valor da VARIAVEL(10) + 10 = 20;

variavel -= 10
# SUBTRAI o valor da VARIAVEL(10) - 10 = 0;

variavel /= 10
# DIVIDE o valor da VARIAVEL(10) / 10 = 1.0 (FLOAT);

variavel //= 10
# DIVIDE INTEIRO o valor da VARIAVEL(10) / 10 = 1 (INT);

variavel *= 10
# MULTIPLICA o valor da VARIAVEL(10) * 10 = 100;

variavel **= 10
# POTENCIALIZA o valor da VARIAVEL(10) ** 10 = 100.000.000,00;

variavel %= 10
# MODULOD, RETORNA O RESTA DA DIVIS√ÉO do valor da VARIAVEL(10) % 10 = 0.0 (FLOAT);


print(variavel)