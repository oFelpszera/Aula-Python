"""
PYTHON É UMA LINGUAGEM DE PROGRAMAÇÃO DO TIPO -> DINAMICA / FORTE
str -> string -> texto
STRINGS SÃO TEXTOS DENTRO DE ASPAS, SEJA SIMPLES '' OU DUPLAS ""
"""
print('Felipe Andrade') # Felipe Andrade é uma STRING com aspas simples
print("Felipe Andrade") # Felipe Andrade é uma STRING com aspas duplas
"""
1. SE EU QUISER COLOCAR ASPAS NA STRING, EU POSSO USAR UMA DIFERENTE DA QUE EU ABRI
2. POSSO UTILIZAR O "ESCAPE" -> \ (O QUE VEM DEPOIS DA BARRA ELE IGNORA)
3. CASO EU QUEIRA USAR A BARRA PARA COMPOR O STRING USO O 'r' (REGULAR EXPRESSION)
"""
print("1 Felipe 'Andrade'"), print('1.2 Felipe "Andrade"')
print("2 Felipe \"Andrade\"") # USEI \" PARA ELE IGNORAR A ASPAS SEGUINTE
print(r"3 Felipe \"Andrade\"")